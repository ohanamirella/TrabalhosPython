print(resultado)
#--- Operação de multiplicação entre dois números inteiros
resultado = numero1 * numero2 
print(resultado)
#--- Operação de divisão entre dois números inteiros
resultado = numero1 / numero2 
print(resultado)
#--- Operação de soma entre duas variávis strings (concatenação)
